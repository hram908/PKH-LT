package pkh.form.materials.abschnitte.a;

public class GesetzVertreter {
    private boolean hatGesetzlichenVertreter;
    private String vertreterName;
    private String vertreterVorname;
    private String vertreterTelefon;
    private String vertreterStrasse;
    private String vertreterHausnummer;
    private String vertreterPlz;
    private String vertreterOrt;

    public GesetzVertreter() {
    }

    public boolean isHatGesetzlichenVertreter() {
        return hatGesetzlichenVertreter;
    }

    public void setHatGesetzlichenVertreter(boolean hatGesetzlichenVertreter) {
        this.hatGesetzlichenVertreter = hatGesetzlichenVertreter;
    }

    public String getVertreterName() {
        return vertreterName;
    }

    public void setVertreterName(String vertreterName) {
        this.vertreterName = vertreterName;
    }

    public String getVertreterVorname() {
        return vertreterVorname;
    }

    public void setVertreterVorname(String vertreterVorname) {
        this.vertreterVorname = vertreterVorname;
    }

    public String getVertreterOrt() {
        return vertreterOrt;
    }

    public void setVertreterOrt(String vertreterOrt) {
        this.vertreterOrt = vertreterOrt;
    }

    public String getVertreterPlz() {
        return vertreterPlz;
    }

    public void setVertreterPlz(String vertreterPlz) {
        this.vertreterPlz = vertreterPlz;
    }

    public String getVertreterHausnummer() {
        return vertreterHausnummer;
    }

    public void setVertreterHausnummer(String vertreterHausnummer) {
        this.vertreterHausnummer = vertreterHausnummer;
    }

    public String getVertreterStrasse() {
        return vertreterStrasse;
    }

    public void setVertreterStrasse(String vertreterStrasse) {
        this.vertreterStrasse = vertreterStrasse;
    }

    public String getVertreterTelefon() {
        return vertreterTelefon;
    }

    public void setVertreterTelefon(String vertreterTelefon) {
        this.vertreterTelefon = vertreterTelefon;
    }
}
